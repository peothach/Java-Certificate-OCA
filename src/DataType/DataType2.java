package DataType;

public class DataType2 {
    public static void main(String[] args) {
        int var = 10;
        char var1 = 10;
        Integer var2 = 10;
        Short var3 = 10;
        byte var4 = 10;

        /**
         * switch case don't allow double and long pass into
         */
        long var5 = 10;
        double var6 = 10;

        switch (var){
            case 10:
                System.out.println("TEN");
                break;
        }
    }
}
